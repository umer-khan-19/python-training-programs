import random

#generate random number between 1 to 100.
random_num = random.randint(1,100)
print(random_num)

#generate random floating number between 0 to 1 .
random_num = random.random()
print(random_num)

#generate random floating number between 1 to 10 .
random_num = random.uniform(1, 10)
print(random_num)
